package egovframework.let.shop.review.service;

public class Review {
	private String s_id;
	private int sns_idx;
	private int r_idx;
	private int p_idx;
	private String r_content;
	private String r_regdate;
	private String r_delyn;
	private int r_div;
	private int r_score;

	public String getS_id() {
		return s_id;
	}

	public void setS_id(String s_id) {
		this.s_id = s_id;
	}

	public int getSns_idx() {
		return sns_idx;
	}

	public void setSns_idx(int sns_idx) {
		this.sns_idx = sns_idx;
	}

	public int getR_idx() {
		return r_idx;
	}

	public void setR_idx(int r_idx) {
		this.r_idx = r_idx;
	}

	public int getP_idx() {
		return p_idx;
	}

	public void setP_idx(int p_idx) {
		this.p_idx = p_idx;
	}

	public String getR_content() {
		return r_content;
	}

	public void setR_content(String r_content) {
		this.r_content = r_content;
	}

	public String getR_regdate() {
		return r_regdate;
	}

	public void setR_regdate(String r_regdate) {
		this.r_regdate = r_regdate;
	}

	public String getR_denyn() {
		return r_delyn;
	}

	public void setR_denyn(String r_denyn) {
		this.r_delyn = r_denyn;
	}

	public int getR_div() {
		return r_div;
	}

	public void setR_div(int r_div) {
		this.r_div = r_div;
	}

	public int getR_score() {
		return r_score;
	}

	public void setR_score(int r_score) {
		this.r_score = r_score;
	}
}
